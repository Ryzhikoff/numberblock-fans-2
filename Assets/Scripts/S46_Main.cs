    public GameObject prefabOne;
    public float offsetY = 5f;
    public float speedBlock = 5f;

    private int counter = 0;
    private Vector3 targetPosition = Vector3.zero;
    private GameObject currentBlock;
    private bool needMoveBlock = false;

    private void Start() {
        //addBlockPositionToList();
        //printAllPosition();
        prepareBock();
    }

    private void prepareBock() {
        targetPosition = positions[counter];
        GameObject block = Instantiate<GameObject>(prefabOne);
        block.transform.position = targetPosition + Vector3.up * offsetY;
        currentBlock = block;
        counter++;
        needMoveBlock = true;
    }

    private void Update() {
        if (needMoveBlock) {
            moveBLock();
        }
    }
    private void moveBLock() {
        currentBlock.transform.position -= Vector3.up * speedBlock * Time.deltaTime;
        if (currentBlock.transform.position.y <= targetPosition.y) {
            currentBlock.transform.position = targetPosition;
            needMoveBlock = false;
            prepareBock();
        }
    }

    private void sortList() {
        positions.Sort();
    }

    private void addBlockPositionToList() {
        GameObject[] blocks = GameObject.FindGameObjectsWithTag("Block");
        foreach (GameObject go in blocks) {
            positions.Add(go.transform.position);
        }
    }

